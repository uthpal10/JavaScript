// undefined
let a;
console.log(a); // undefined
a = 'Now it is defined'
console.log(a);
console.log(typeof(a)); // string

// number
let b = 20
console.log(b); // 20
b = 20.20
console.log(b); // 20.20
console.log(typeof(b)); // number

// string
const c = 'uthpal'
console.log(c);

// boolean
let d = true
console.log(d);
d = false
console.log(d);
console.log(typeof(d)); // boolean

// null
let e = null;
console.log(e);
console.log(typeof(e)); // object

e = 'ramesh'
console.log(e); // ramesh