// The ternary operator, also known as conditional operator,
// It is concise way to write conditional expressions in JavaScript. 
// It provides a shorthand syntax for performing a conditional check and 
// returning different values based on whether the condition is true or false.
// Syntax < condition ? expression 1 : expression 2 >

let age = 25;

let vote = age>=18 ? 'Eligibile To Cast Vote' : 'NotEligibile To Cast Vote';

console.log(vote);
