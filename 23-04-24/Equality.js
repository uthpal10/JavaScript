// Loose Equality
console.log(21 == '21');
console.log(21 == 21);
console.log(true == 1);
console.log(false == 0);

// Strict Equality
console.log(21 === '21');
console.log(true === true);
console.log(false === false);
console.log('uthpal' === "uthpal");

let a = 23;

let b ='23';

console.log(a === b);