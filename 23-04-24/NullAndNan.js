// null is a primitive value that represents the intentional absence of any object value.
// It is often used to indicate that a variable or object property has no value or is empty.

// NaN is a special value in JavaScript that represents "Not-a-Number."
// It is returned when an arithmetic operation or mathematical function produces a result that is not a valid number.

let arr = null;
console.log(arr);

let abb = 10/'uthpal';
console.log(abb);

// null is a primitive value of type object,
// while NaN is a special value of type number.

console.log(null === null); // true
console.log(NaN === NaN); // false

